#include <iostream>

int main()
{
    unsigned long u11 = 3; // 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000010
    unsigned long u12 = 7; // 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000111

    std::cout << (u11 & u12) << std::endl; // 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000010
    std::cout << (u11 | u12) << std::endl; // 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000111

    std::cout << ((u11 && u12) ? "true" : "false") << std::endl; // true, not any 0
    std::cout << ((u11 || u12) ? "true" : "false") << std::endl; // true, not any 0

    return 0;
}